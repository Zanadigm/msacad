                <h1>The Mombasa Academy Anti-Bullying Policy</h1>
                <h3>BULLYING: OUR SCHOOL’S VALUES AND BELIEFS</h3>
                <ul>
                    <li>All pupils and staff have the right to feel happy, safe and included.</li>
                    <li>Pupils and staff have the right to work in an environment without harassment, intimidation or fear.</li>
                    <li>All bullying, of any sort, is therefore unacceptable.</li>
                    <li>Pupils who experience bullying will be supported.</li>
                    <li>We recognise the effects that bullying can have on pupils’ feelings of self-worth and on their school work. The school community will actively promote an anti-bullying environment.</li>
                </ul>
                <h3>OUR INTENTIONS IN PRODUCING THIS POLICY</h3>
                <ul>
                    <li>To express our belief that all pupils should be included fully in the life of the school.</li>
                    <li>To provide a learning environment free from any threat or fear, conducive to the achievement of individual aspirations.</li>
                    <li>To reduce and to eradicate wherever possible, instances in which pupils are made to feel frightened, excluded or unhappy.</li>
                    <li>To reduce and to eradicate wherever possible, instances in which pupils are subject to any form of bullying. </li>
                    <li>To respond effectively to all instances of bullying that are reported to us.</li>
                    <li>To establish a means of dealing with bullying, and of providing support to pupils who have been bullied. </li>
                    <li>To provide support for pupils who are accused of bullying, who may be experiencing problems of their own.</li>
                    <li>To ensure that all pupils and staff are aware of the policy and that they fulfil their obligations to it.</li>
                </ul>
                <h3>OUR DEFINITION OF BULLYING</h3>
                <p>We define bullying as the willful desire to hurt, threaten or frighten another person.</p>
                <p>Bullying is: </p>
                <ul>
                    <li>Deliberately hurtful</li>
                    <li>Repeated, often over a period of time</li>
                    <li>Difficult for victims to defend themselves against</li>
                </ul>
                <p>It can take many forms but the main types are:</p>
                <ul>
                    <li>Physical – hitting, kicking, taking another’s belongings </li>
                    <li>Verbal – name calling, insulting, making offensive remarks </li>
                    <li>Indirect – spreading nasty stories about someone, exclusion from social groups, being made the subject of malicious rumours</li>
                </ul>
                <p>Some forms of bullying are attacks not only on the individual, but also on the group to which he or she may belong. Within school we will pay particular attention to:</p>
                <ul>
                    <li>Racial harassment and racist bullying </li>
                    <li>Bullying because of pupils’ religious beliefs </li>
                    <li>Bullying based on gender issues </li>
                    <li>Bullying of pupils who have special educational needs or disabilities.</li>
                </ul>
                <h3>OUR EXPECTATIONS</h3>
                <p><strong>Everyone within school is expected to</strong> </p>
                <ul>
                    <li>Act in a respectful and supportive way towards one another</li>
                    <li>Adhere to and to promote the objectives of this policy</li>
                </ul>
                <p><strong>Pupils are expected to</strong></p>
                <ul>
                    <li>Report all incidents of bullying,</li>
                    <li>Report suspected incidents that victims may be afraid to report.</li>
                    <li>Support each other and to seek help to ensure that everyone feels safe and nobody feels excluded or afraid in school.</li>
                </ul>
                <p><strong>Parents can help by: </strong></p>
                <ul>
                    <li>Supporting our anti-bullying policy and procedures.</li>
                    <li>Encouraging their children to be positive members of the school community.</li>
                    <li>Discussing with their child’s teacher any concerns that their child may be experiencing bullying or involved in some other way.</li>
                    <li>Helping to establish an anti-bullying culture outside of school.</li>
                </ul>
                <h3>WHAT HAPPENS IF BULLYING TAKES PLACE AT THE MOMBASA ACADEMY?</h3>
                <p>Formally, in the first instance, the Form Tutors of all children concerned are notified. If the Form Tutor feels that the gravity of the incident warrants intervention from other staff (Head of School, Headmaster) then details are forwarded accordingly.</p>
                <p>We do feel that in the first instance, our students should feel comfortable and able to talk to a member of staff about any issues of this nature so that a solution can be found as quickly as possible.</p>
                <p>Any incidents involving bullying will be dealt with in the strictest manner. </p>
                <h3>BULLYING CAN TAKE PLACE OUTSIDE THE SCHOOL PREMISES</h3>
                <p>Schools are not directly responsible for bullying that occurs off the premises but we know that bullying can occur outside the school. The bullying may be done by pupils from our own school, by pupils from other schools or by people who are not at school at all. Where a pupil or parent tells us of bullying off the school premises we will:</p>
                <ul>
                    <li>Talk to pupils about how to avoid or handle bullying outside of school.</li>
                    <li>Talk to the Headteacher of other schools whose pupils are bullying.</li>
                </ul>
                <p>We also recognise that there may be times when parents feel that we have not dealt well with an incident of bullying and we would ask that this be brought to the Headteacher’s notice, so that appropriate action may be taken. </p>
