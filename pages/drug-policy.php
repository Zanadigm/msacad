                <h1>The Mombasa Academy Drug Policy</h1>
                <h3>Introduction – Principles</h3>
                <ul>
                    <li>The school does not condone the misuse of drugs by members of the school or the illegal supply of these substances </li>
                    <li>The school believes it has a duty to inform and educate young people on the consequences and risks of drug, alcohol and substance use and misuse. </li>
                    <li>The values and ethos of the school should enable pupils to develop control over their own lives so that they have the confidence to make decisions which will lead to a healthy lifestyle </li>
                    <li>The school accepts the importance of its pastoral role in the welfare of young people and will provide appropriate support for their differing needs.</li>
                </ul>
                <h3>Aims</h3>
                <ul>
                    <li>To develop positive pro-active attitudes, values and skills in the pupils to equip them to make appropriate informed decisions about their personal patterns of behaviour and lifestyles </li>
                    <li>To provide, if necessary, accurate information about potentially harmful substances </li>
                    <li>As appropriate, to inform pupils and increase understanding about the implications and possible consequences of use and misuse </li>
                </ul>
                <h3>DRUGS</h3>
                <h4>A DEFINITION</h4><strong><em>A drug is any substance the use of which alters the physiological functioning of the body.</em></strong>
                <p>These fall into three main categories:</p>
                <ul>
                    <li>Those it is legal to possess, supply or use</li>
                    <li>Those it is illegal to possess, supply or use – e.g. cannabis, Ecstasy, LSD, amphetamines, cocaine, heroin etc</li>
                    <li>Those it is legal to obtain as prescribed medicines but which have the potential to cause harm if misused or obtained illegally</li>
                </ul>
                <h3>PHILOSOPHY </h4>
                    <p>The school acknowledges that the numbers of young people using and misusing substances are rising but also recognises that larger numbers of young people are choosing NOT to use or misuse substances.</p>
                    <ul>
                        <li>In instances involving substance misuse, the Headteacher will inform parents at the earliest opportunity. Substance misuse by pupils in school means using, supplying or preparing drugs on school premises, including alcohol and tobacco. </li>
                        <li>The school will consider each substance incident individually and recognises that a variety of responses may be appropriate to deal with incidents. The school will consider very carefully the implications of any action that may be taken as it seeks to balance the needs of the pupil involved, their parents, other pupils and the interests of the local community. </li>
                        <li>In instances involving suspected or actual substance misuse or supply on the school premises ALL members of staff should follow the lines of action below </li>
                    </ul>
                    <h3>PROCEDURES </h3>
                    <ul>
                        <li>Staff who become aware of students being in possession of drugs, or using drugs on school premises should inform The Head of School of the possible signs of substance misuse</li>
                        <li>If appropriate, First Aid should be administered by a qualified member of staff. </li>
                        <li>Where staff discover substances, before the Headteacher has been contacted, which are suspected to be harmful, illegal or needing investigation, they should be removed as follows: <ul>
                                <li>From a place - Remove the substance, if possible in front of a witness (colleague or pupil).<strong> DO NOT</strong> , however, leave the substance even if a witness is not available</li>
                                <li>From a person - Remove or receive substances from a pupil in front of a witness.</li>
                            </ul>
                        </li>
                    </ul>
                    <p>If in the professional judgement of the member of staff, the substance may be harmful, it should be removed or received without a witness. </p>
                    <ul>
                        <li>The child (and the substance) should be taken immediately to the Headteacher. </li>
                        <li>The Headteacher will place the substance in a sealed container which will be signed and dated by himself and the member of staff.</li>
                        <li>The Headteacher will meet with the appropriate Head of School to discuss the pupil and the incident, and agree upon an appropriate plan of action. </li>
                        <li>The Headteacher will interview the pupil and, where appropriate, any witnesses, and explain why the investigation is taking place. </li>
                        <li>Parents will be informed at the earliest opportunity by the Headteacher. </li>
                        <li>Appropriate further courses of action may include: <p>Contact with parents by </p>
                            <ul>
                                <li>Letter </li>
                                <li>Phone call </li>
                                <li>Visit to the school </li>
                            </ul>
                        </li>
                    </ul>
                    <h3>SPECIFIC ISSUES </h3>
                    <dl>
                        <dt><strong> Medicines in School</strong></dt>
                        <dd>Parents of any pupil on prescribed medication should inform the school of its usage and dosage and, with the exception of asthma inhalers, the medication should be given to the School Office for safe keeping during the day. </dd>
                        <dt><strong>Parental Involvement</strong> </dt>
                        <dd>Parents will be involved at the earliest possible time in any drugs related incident, and kept well informed of subsequent actions.</dd>
                        <dd>The Pastoral Care structure will always seek to help individual parents who have concerns regarding drug, alcohol or substance misuse. </dd>
                        <dt><strong>Pupils reporting drug related incidents</strong> </dt>
                        <dd>All pupils should be encouraged to discuss concerns regarding drug, alcohol or substance misuse with any member of staff with whom they feel confident enough to do so. </dd>
                    </dl>
                    <h3>SANCTIONS</h3>
                    <p>Any incidents involving drug abuse will be dealt with in the strictest manner. Abuse of illegal substances will result in expulsion from the school with immediate effect.</p>
